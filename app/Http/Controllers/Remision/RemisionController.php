    {
        //

        $rem = Remision::with(
            [
                'factura'=>function($query){
                    $query->select('*');
                    $query->where('Factura_Venta.Estado', '!=', 'Anulada');
                    $query->whereNull('Factura_Venta.Nota_Credito');
                }
            ]
        )->find($id);

        return response()->json($rem);
    }

